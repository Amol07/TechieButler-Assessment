import Foundation

/// Enum representing HTTP methods.
enum HTTPMethod: String {
	/// GET method for fetching resources.
	case get = "GET"
	/// POST method for creating resources.
	case post = "POST"
	/// PUT method for updating resources.
	case put = "PUT"
	/// DELETE method for deleting resources.
	case delete = "DELETE"
}
